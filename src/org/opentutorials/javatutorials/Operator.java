package org.opentutorials.javatutorials;

public class Operator {

	public static void main(String[] args) {
		int i = 3;
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i);
		
		int a = 4-3*6;
		System.out.println(a);
	}

}
